namespace Clases
{
    public class Empleado {

        public int id;
        public string nombre;
        public float sueldo;

        public Empleado() {
        }

        public Empleado(int id, string nombre, float sueldo) {
            this.id = id;
            this.nombre = nombre;
            this.sueldo = sueldo;
        }

    }
}
